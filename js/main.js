const subtract = (a, b) => a - b;
const multiply = (a, b) => a * b;
const divide = (a, b) => {
  if (b === 0) {
    return '–¥—ñ–ª–µ–Ω–Ω—è –Ω–∞ –Ω—É–ª—å!';
  }
  return a / b;
};

// –¢–µ—Å—Ç—É–≤–∞–Ω–Ω—è
