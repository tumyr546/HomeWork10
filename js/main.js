// –°—Ç–≤–æ—Ä—ñ—Ç—å –º—ñ–Ω—ñ–º—É–º 3 —Ä—ñ–∑–Ω—ñ –∫–æ–ª–±–µ–∫-—Ñ—É–Ω–∫—Ü—ñ—ó –¥–ª—è –æ–±—Ä–æ–±–∫–∏ —Ñ—ñ–ª—å–º—ñ–≤
// –ü—Ä–æ—Ç–µ—Å—Ç—É–π—Ç–µ –≤—Å—ñ –≤–∞—Ä—ñ–∞–Ω—Ç–∏

function processMovies(movies, action) {
  for (let i = 0; i < movies.length; i++) {
    action(movies[i], i);
  }
}

const movies = ['–ú–∞—Ç—Ä–∏—Ü—è', '–Ü–Ω—Ç–µ—Ä—Å—Ç–µ–ª–ª–∞—Ä', '–¢–µ–º–Ω–∏–π –ª–∏—Ü–∞—Ä', '–í–æ–ª–æ–¥–∞—Ä –ø–µ—Ä—Å–Ω—ñ–≤'];

// –ö–æ–ª–±–µ–∫–∏:
const logMovie = (movie, index) => console.log(`#${index + 1}: ${movie}`);
const shoutMovie = (movie) => console.log(movie.toUpperCase() + '!');
