// –°—Ç–≤–æ—Ä—ñ—Ç—å —Å—Ç—Ä—ñ–ª–∫–æ–≤—É —Ñ—É–Ω–∫—Ü—ñ—é calculate(a, b, operation)
// –§—É–Ω–∫—Ü—ñ—è –ø–æ–≤–∏–Ω–Ω–∞ –≤–∏–∫–ª–∏–∫–∞—Ç–∏ operation(a, b) —Ç–∞ –ø–æ–≤–µ—Ä–Ω—É—Ç–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç
